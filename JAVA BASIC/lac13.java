//the program for the java operators
import java.util.Scanner;

public class lac13 {
    public static void main(String[] args) {
        System.out.println("Enter the number a and b");
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
    }

}